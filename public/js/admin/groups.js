// Group Recap
// Lets admins pick a WhatsApp group + time period and get an AI-generated
// summary without anything being visible on WhatsApp itself.

async function loadGroups() {
  const AS = window.AdminState;
  const select = document.getElementById('recap-group-select');
  if (!select) return;

  try {
    const groups = await apiFetch('/crm/groups');
    AS.recapGroups = groups;

    if (!groups.length) {
      select.innerHTML = '<option value="">No groups found — messages must be received first</option>';
      return;
    }

    select.innerHTML = groups.map(g =>
      `<option value="${g.id}">${escHtml(g.name)} (${g.count} msg${g.count !== 1 ? 's' : ''})</option>`
    ).join('');

    updateGroupStats();
  } catch {
    select.innerHTML = '<option value="">Failed to load groups</option>';
    showToast('Failed to load groups', 'error');
  }
}

function updateGroupStats() {
  const AS = window.AdminState;
  const select = document.getElementById('recap-group-select');
  const stats  = document.getElementById('recap-group-stats');
  if (!select || !stats) return;

  const group = (AS.recapGroups || []).find(g => g.id === select.value);
  if (group) {
    const last = group.lastMessage ? new Date(group.lastMessage).toLocaleString() : '—';
    stats.textContent = `${group.count} messages stored · Last activity: ${last}`;
    stats.classList.remove('hidden');
  } else {
    stats.classList.add('hidden');
  }
}

async function generateRecap() {
  const groupId = document.getElementById('recap-group-select')?.value;
  const period  = document.getElementById('recap-period-select')?.value || '24h';

  if (!groupId) { showToast('Please select a group', 'error'); return; }

  const btn        = document.getElementById('recap-btn');
  const resultCard = document.getElementById('recap-result-card');
  const empty      = document.getElementById('recap-empty');
  const body       = document.getElementById('recap-result-body');
  const meta       = document.getElementById('recap-result-meta');
  const title      = document.getElementById('recap-result-title');
  const badge      = document.getElementById('recap-provider-badge');
  const badgeText  = document.getElementById('recap-provider-text');

  // Loading state
  btn.disabled = true;
  btn.innerHTML = '<i class="fas fa-circle-notch fa-spin text-xs"></i> Generating…';
  resultCard.classList.add('hidden');
  badge.classList.add('hidden');
  empty.classList.add('hidden');

  try {
    const data = await apiFetch(
      '/crm/groups/' + encodeURIComponent(groupId) + '/recap',
      'POST',
      { period }
    );

    if (!data.summary) {
      empty.innerHTML =
        '<i class="fas fa-inbox text-5xl mb-3 block"></i>' +
        '<p class="text-sm font-medium">No messages found in this group for that period</p>';
      empty.classList.remove('hidden');
      return;
    }

    // Provider badge
    const providerNames = { chat: 'Gemini', gpt: 'ChatGPT', claude: 'Claude' };
    badgeText.textContent =
      `Summary generated by ${providerNames[data.provider] || data.provider} · ${data.count} messages analysed`;
    badge.classList.remove('hidden');

    // Result
    title.textContent  = `Recap — ${data.groupName}`;
    meta.textContent   = `${data.label} · ${data.count} messages`;
    body.dataset.raw   = data.summary;          // plain text for clipboard copy
    body.innerHTML     = renderRecapMarkdown(data.summary);
    resultCard.classList.remove('hidden');

  } catch {
    showToast('Failed to generate recap', 'error');
    empty.innerHTML =
      '<i class="fas fa-exclamation-circle text-5xl mb-3 block"></i>' +
      '<p class="text-sm font-medium">Failed to generate recap. Check your AI API key in Settings.</p>';
    empty.classList.remove('hidden');
  } finally {
    btn.disabled = false;
    btn.innerHTML = '<i class="fas fa-magic text-xs"></i> Generate Recap';
  }
}

function copyRecap() {
  // Copy the original plain-text stored in the data attribute, not the innerHTML
  const el = document.getElementById('recap-result-body');
  const text = el?.dataset.raw || el?.innerText || '';
  navigator.clipboard.writeText(text)
    .then(() => showToast('Copied to clipboard', 'success'))
    .catch(() => showToast('Copy failed', 'error'));
}

/**
 * Converts AI markdown output to safe HTML.
 * Order matters: escape first, then apply patterns so we never inject raw AI text.
 *   **text**  → <strong class="recap-heading">text</strong>
 *   *text*    → <em>text</em>
 *   - item    → • item  (at line start)
 *   \n        → <br>
 */
function renderRecapMarkdown(raw) {
  // 1. Escape HTML to prevent any XSS from AI-generated content
  let html = String(raw)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');

  // 2. **bold** → styled heading (section headers)
  html = html.replace(/\*\*([^*\n]+?)\*\*/g, '<strong class="recap-heading">$1</strong>');

  // 3. *italic* (single asterisks, not adjacent to another *)
  html = html.replace(/\*([^*\n]+?)\*/g, '<em>$1</em>');

  // 4. Bullet lines: "- text" at the start of a line
  html = html.replace(/(^|\n)- /g, '$1<span class="recap-bullet">•</span> ');

  // 5. Newlines → <br>
  html = html.replace(/\n/g, '<br>');

  return html;
}

// Small XSS-safe helper
function escHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
